  @doc """
  Idle/breathing animation keyframes - subtle breathing and weight shifting.
  """
  def idle_keyframes do
    [
      %{time: 0.0, landmarks: base_standing_pose()},
      %{
        time: 0.25,
        landmarks: apply_breathing(base_standing_pose(), 0.005, 0.003)
      },
      %{
        time: 0.5,
        landmarks: apply_breathing(base_standing_pose(), 0.0, 0.006)
      },
      %{
        time: 0.75,
        landmarks: apply_breathing(base_standing_pose(), -0.005, 0.003)
      }
    ]
  end

  defp apply_breathing(pose, sway, breath) do
    Enum.with_index(pose)
    |> Enum.map(fn {lm, idx} ->
      if idx in 0..22 do
        %{lm | x: lm.x + sway, y: lm.y - breath}
      else
        lm
      end
    end)
  end

  @doc """
  Get keyframes for a specific motion type.
  """
  def get_keyframes(motion_type) do
    case motion_type do
      :walking -> walking_keyframes()
      :dancing -> dancing_keyframes()
      :exercise -> exercise_keyframes()
      :waving -> waving_keyframes()
      :idle -> idle_keyframes()
      _ -> idle_keyframes()
    end
  end

  @doc """
  Get the duration of one animation cycle in seconds.
  """
  def cycle_duration(motion_type) do
    case motion_type do
      # 1 second per full gait cycle
      :walking -> 1.0
      # 4 seconds for dance sequence
      :dancing -> 4.0
      # 3 seconds per pushup
      :exercise -> 3.0
      # 1.5 seconds per wave cycle
      :waving -> 1.5
      # 4 seconds for breathing cycle
      :idle -> 4.0
      _ -> 4.0
    end
  end
